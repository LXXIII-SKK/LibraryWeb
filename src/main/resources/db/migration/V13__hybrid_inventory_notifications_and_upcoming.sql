create table library_location (
    id bigint generated by default as identity primary key,
    branch_id bigint not null references library_branch(id),
    code varchar(30) not null,
    name varchar(120) not null,
    floor_label varchar(50),
    zone_label varchar(100),
    active boolean not null default true,
    created_at timestamp with time zone not null default current_timestamp,
    updated_at timestamp with time zone not null default current_timestamp,
    constraint uq_library_location_branch_code unique (branch_id, code)
);

create table book_holding (
    id bigint generated by default as identity primary key,
    book_id bigint not null references book(id) on delete cascade,
    branch_id bigint not null references library_branch(id),
    location_id bigint references library_location(id),
    format varchar(20) not null,
    total_quantity int not null,
    available_quantity int not null,
    access_url varchar(500),
    active boolean not null default true,
    created_at timestamp with time zone not null default current_timestamp,
    updated_at timestamp with time zone not null default current_timestamp,
    constraint chk_book_holding_quantities check (
        total_quantity >= 0
        and available_quantity >= 0
        and available_quantity <= total_quantity
    )
);

alter table borrow_transaction
    add column holding_id bigint references book_holding(id);

create table upcoming_book (
    id bigint generated by default as identity primary key,
    title varchar(255) not null,
    author varchar(255) not null,
    category varchar(100),
    isbn varchar(50),
    summary varchar(600),
    expected_at timestamp with time zone not null,
    branch_id bigint references library_branch(id),
    created_at timestamp with time zone not null default current_timestamp,
    updated_at timestamp with time zone not null default current_timestamp
);

create table upcoming_book_tag (
    id bigint generated by default as identity primary key,
    upcoming_book_id bigint not null references upcoming_book(id) on delete cascade,
    name varchar(50) not null,
    constraint uq_upcoming_book_tag unique (upcoming_book_id, name)
);

create table staff_notification (
    id bigint generated by default as identity primary key,
    title varchar(120) not null,
    message varchar(600) not null,
    created_by_user_id bigint not null references app_user(id),
    branch_id bigint references library_branch(id),
    created_at timestamp with time zone not null default current_timestamp
);

create table staff_notification_target_role (
    notification_id bigint not null references staff_notification(id) on delete cascade,
    target_role varchar(30) not null,
    constraint uq_staff_notification_target_role unique (notification_id, target_role)
);

create table staff_notification_receipt (
    id bigint generated by default as identity primary key,
    notification_id bigint not null references staff_notification(id) on delete cascade,
    user_id bigint not null references app_user(id) on delete cascade,
    read_at timestamp with time zone not null,
    constraint uq_staff_notification_receipt unique (notification_id, user_id)
);

create index idx_library_location_branch on library_location(branch_id);
create index idx_book_holding_book on book_holding(book_id);
create index idx_book_holding_branch on book_holding(branch_id);
create index idx_upcoming_book_expected_at on upcoming_book(expected_at);
create index idx_staff_notification_created_at on staff_notification(created_at desc);

